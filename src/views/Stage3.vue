<template>
  <div id="stage3">
    <!--修改下方的MessageList和PostDialog，注意做好数据绑定-->
    <MessageList :messageList = "this.messageList"/>
    
    <template v-if="dialogVisible"  >
      <PostDialog :dialogVisible = "true" :state = "state" @changePostDialogData="changePost"/>
    </template>
  </div>
</template>

<script>
import MessageList from "@/components/MessageList.vue"
import PostDialog from "@/components/PostDialog.vue"
import CookieOperation from "@/utils/tools";
export default {
	name: 'Stage3',
	components: {
		MessageList,
		PostDialog
	},
  mounted(){
    CookieOperation.setCookie("username","Alice", 1e9) //仅用于测试PostDialog
    CookieOperation.setCookie("name","Alice", 1e9) //仅用于测试PostDialog
    CookieOperation.setCookie("user","Alice", 1e9) //仅用于测试PostDialog
    this.state.username = "Alice"
		this.getAjax();
    //this.setUserName("Alice")
  },
	// 请在下方设计自己的数据结构和函数，例如其中messageList是用于保存从后端获取到的消息列表，dialogVisible可以用于标识对话框是否显示（参见Vue对话框组件的属性列表）
	data(){
		return {
			messageList: [],
			dialogVisible: true,
      state:{
        username:"",
        username_valid: false,
      }
		}
	},
	methods: {
		get_function :function(data) {
      this.messageList = []
			for (let i in data) {
        if (data[i].timestamp > 1e11) 
          data[i].timestamp /= 1000
        data[i].user = unescape(data[i].user)
				this.messageList.push({
					title: data[i].title,
					message: data[i].content,
					user: data[i].user,
					timestamp: data[i].timestamp
            
				})
			}
    },
		getAjax :function () {
      this.$axios.request_json(this.get_function)
		},
    changePost(title, messages) {
      this.dialogVisible = false;
      if (title == null){
        return;
      }
      this.$axios.send_json(title, messages ).then((str) => {
        this.getAjax()
        this.$message(str)
      })
    },
    async setUserName(name) {
      this.state.username = name
      this.state.username_valid = true
      await CookieOperation.setCookie("name", name, new Date().getTime() + 1e9)
      this.loginDialog.dialogVisible = false
      this.loginDialog.form.username = ""
    }
	},
  watch: { // 用于实时检测username是否合法
    "state.username": {
      handler(newName) {
        this.state.username_valid = /^[A-Za-z\u4e00-\u9fa5][-A-Za-z0-9\u4e00-\u9fa5_]*$/.test(newName)
      }
    }
  }
}
</script>